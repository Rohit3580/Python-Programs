import numpy as np

print("1D array operatn:")

array1 = np.array([12,14,58,96,22])
print(array1)
imean = np.mean(array1)
print("Mean of array1 is:",imean)
print("Median of array1 is:",np.median(array1))
print("Max if array1 is:",np.max(array1))
print("Min value of array1 is:",np.min(array1))
print("Sum of array1 is:",np.sum(array1))
print("Product of array1 is:",np.prod(array1))
print("Variance of array1:",np.var(array1))
print("standard deviation of array1:",np.std(array1))